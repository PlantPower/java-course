public class whileloops {

    //While loop without a stop.
    //Infinity

    public static void main(String[] args){
        int i = 10;
        while(i > 1){
            System.out.println(i);
            i++;
        }

        //Countdown from 10 to 2 loop

        int j = 10;
        while(j > 1){
            System.out.println(j);
            j--;
        }

    }

}
