public class operators {

    public static void main(String[] args){
        int n1 = 100;
        int n2 = 20;

        System.out.println("N1 + N2 = " + (n1 + n2));
        System.out.println("N1 - N2 = " + (n1 - n2));
        System.out.println("N1 / N2 = " + (n1 / n2));
        System.out.println("N1 * N2 = " + (n1 * n2));
        System.out.println("N1 % N2 = " + (n1 % n2));

    }
}
