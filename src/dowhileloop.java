public class dowhileloop {

    public static void main(String[] args){

        //Countdown from 10 to 2

        int i = 10;
        do{
            System.out.println(i);
            i--;
        }
        while (i > 1);

        //Infinity loop

        int j = 10;
        do{
            System.out.println(j);
            j--;
        }
        while (j > 1);
    }

}
